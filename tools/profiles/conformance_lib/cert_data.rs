use kryoptic_lib::pkcs11::*;
use std::sync::LazyLock;

pub(crate) struct ObjAttr {
    pub(crate) atype: CK_ATTRIBUTE_TYPE,
    pub(crate) adata: Vec<u8>,
}

pub(crate) struct CertTmpl {
    pub(crate) attrs: [ObjAttr; 11],
}

macro_rules! encode {
    (ulong, $val:expr) => {{
        Vec::from($val.to_ne_bytes())
    }};
    (bbool, $val:expr) => {{
        vec![$val as u8]
    }};
    (string, $val:expr) => {{
        $val.to_bytes_with_nul().to_vec()
    }};
}

